package sample;

import java.util.ArrayList;

public class Track {

    // ---------------------------------------------------------------- Variables ---------------------------------------------------------------------------
    ArrayList<Line> line_ArrayList;
    String name = "";
    // ---------------------------------------------------- Constructors, Getters and Setters ---------------------------------------------------------------
    public Track(String param_File_Name){
        line_ArrayList = new ArrayList<Line>();
        name = param_File_Name;
    }
    // ------------------------------------------------------------- Private/Helper Functions ---------------------------------------------------------------
    // ------------------------------------------------------------ Miscellaneous ---------------------------------------------------------------------------
    //*******************************************************************************************************************************************************
    // Adds a line to the Track                                                                                                                             *
    //*******************************************************************************************************************************************************
    public void add_Line(Line param_Line){
        line_ArrayList.add(param_Line);
    }
    //*******************************************************************************************************************************************************
    // Returns the line from the specified index                                                                                                            *
    //*******************************************************************************************************************************************************
    public Line get_Line_At_Index(int param_Line_Index){
        return line_ArrayList.get(param_Line_Index);
    }
}
